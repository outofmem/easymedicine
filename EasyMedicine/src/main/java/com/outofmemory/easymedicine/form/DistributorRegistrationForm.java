package com.outofmemory.easymedicine.form;

/**
 * The distributor registration form
 * 
 * @author pribiswas
 * 
 */
public class DistributorRegistrationForm {

	private String emailAddress;
	private String password;
	private String mobileNumber;
	private String medicineStoreName;
	private String medicineStoreAddress;
	private String medicineStoreLocality;
	private String medicineStoreCity;
	private String medicineStorePin;

	/**
	 * @return the emailAddress
	 */
	public String getEmailAddress() {
		return emailAddress;
	}

	/**
	 * @param emailAddress
	 *            the emailAddress to set
	 */
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * @param password
	 *            the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	/**
	 * @return the mobileNumber
	 */
	public String getMobileNumber() {
		return mobileNumber;
	}

	/**
	 * @param mobileNumber
	 *            the mobileNumber to set
	 */
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	/**
	 * @return the medicineStoreName
	 */
	public String getMedicineStoreName() {
		return medicineStoreName;
	}

	/**
	 * @param medicineStoreName
	 *            the medicineStoreName to set
	 */
	public void setMedicineStoreName(String medicineStoreName) {
		this.medicineStoreName = medicineStoreName;
	}

	/**
	 * @return the medicineStoreAddress
	 */
	public String getMedicineStoreAddress() {
		return medicineStoreAddress;
	}

	/**
	 * @param medicineStoreAddress
	 *            the medicineStoreAddress to set
	 */
	public void setMedicineStoreAddress(String medicineStoreAddress) {
		this.medicineStoreAddress = medicineStoreAddress;
	}

	/**
	 * @return the medicineStoreLocality
	 */
	public String getMedicineStoreLocality() {
		return medicineStoreLocality;
	}

	/**
	 * @param medicineStoreLocality
	 *            the medicineStoreLocality to set
	 */
	public void setMedicineStoreLocality(String medicineStoreLocality) {
		this.medicineStoreLocality = medicineStoreLocality;
	}

	/**
	 * @return the medicineStoreCity
	 */
	public String getMedicineStoreCity() {
		return medicineStoreCity;
	}

	/**
	 * @param medicineStoreCity
	 *            the medicineStoreCity to set
	 */
	public void setMedicineStoreCity(String medicineStoreCity) {
		this.medicineStoreCity = medicineStoreCity;
	}

	/**
	 * @return the medicineStorePin
	 */
	public String getMedicineStorePin() {
		return medicineStorePin;
	}

	/**
	 * @param medicineStorePin
	 *            the medicineStorePin to set
	 */
	public void setMedicineStorePin(String medicineStorePin) {
		this.medicineStorePin = medicineStorePin;
	}

}
